export function EmojiPicker({ onEmojiSelect }: EmojiPickerProps) {
  const [isOpen, setIsOpen] = useState(false)
  const [activeCategory, setActiveCategory] = useState<keyof typeof emojiCategories>('smileys')

  const handleEmojiClick = (emoji: string) => {
    onEmojiSelect(emoji)
    setIsOpen(false)
  }

  return (
    <Popover open={isOpen} onOpenChange={setIsOpen}>
      <PopoverTrigger asChild>
        <Button
          type="button"
          variant="ghost"
          size="sm"
          className="h-8 w-8 p-0"
        >
          <Smile className="h-4 w-4" />
        </Button>
      </PopoverTrigger>
      <PopoverPortal>
        <PopoverContent
          align="start"
          sideOffset={5}
          onOpenAutoFocus={(e) => e.preventDefault()}
        >
          <div className="space-y-2">
            {/* Category tabs */}
            <div className="flex gap-1 border-b pb-2">
              {Object.keys(emojiCategories).map((category) => (
                <button
                  key={category}
                  onClick={() => setActiveCategory(category as keyof typeof emojiCategories)}
                  className={cn(
                    'px-2 py-1 text-xs rounded-md transition-colors',
                    activeCategory === category
                      ? 'bg-primary text-primary-foreground'
                      : 'hover:bg-accent'
                  )}
                >
                  {category === 'smileys' && 'ğŸ˜Š'}
                  {category === 'hearts' && 'â¤ï¸'}
                  {category === 'gestures' && 'ğŸ‘'}
                  {category === 'objects' && 'ğŸ‰'}
                  {category === 'symbols' && 'âœ…'}
                </button>
              ))}
            </div>
            
            {/* Emoji grid */}
            <div className="grid grid-cols-8 gap-1 max-h-64 overflow-y-auto">
              {emojiCategories[activeCategory].map((emoji, index) => (
                <button
                  key={`${activeCategory}-${index}`}
                  onClick={() => handleEmojiClick(emoji)}
                  className="text-xl p-2 hover:bg-accent rounded-md transition-colors"
                  title={emoji}
                >
                  {emoji}
                </button>
              ))}
            </div>
          </div>
        </PopoverContent>
      </PopoverPortal>
    </Popover>
  )
}
